  IF (OSTAT .NE. OSTATFIR) THEN
    FIRST1=.FALSE.
  END IF
  IF (CUTNUMQ .NE. CUTNUMQFIR) THEN
    FIRST1=.FALSE.
  END IF
END IF

IF (.NOT. FIRST1) THEN
  CALL INFO(OSTAT,CUTNUMQ)
  OSTATFIR=OSTAT
  CUTNUMQFIR=CUTNUMQ
  CALL TRNSCH(NXP/2,NXP/2,NXP,LAMXP1,LAMXP2,LAMXP3,XP,XW)
