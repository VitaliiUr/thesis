SUBROUTINE CHIRALMOMPWD(OSTAT,FORCE,PPUN1,PPUN2,JMOM,CUTNUMQ,POTEN)
IMPLICIT NONE
INTEGER                    ::I,J,OSTAT,JMOM,CUTNUMQ
