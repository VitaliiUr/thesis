SUBROUTINE CHIRALMOMLR(OSTAT,QPUN,CUTNUMQ,POTMOM)
IMPLICIT NONE
INTEGER                    ::I,J,OSTAT,CUTNUMQ
REAL(KIND(0.0D0))          ::QPUN,POTMOM(NSPIN,NISO),OPEPMOM(NSPIN,NISO),&
                             TPEPPOLMOM(NSPIN,NISO)

